// Создаем массив с 4 элементами
let array = [1, 2, 3, 4];

// Выводим длину массива в console.log и alert
console.log("Длина массива: " + array.length);
alert("Длина массива: " + array.length);

// Подтверждаем удаление элемента при нажатии кнопки "ОК"
let confirmation = confirm("Нажмите 'ОК', чтобы удалить один элемент из массива.");
if (confirmation) {
    array.pop(); // Удаляем последний элемент из массива
    console.log("Массив после удаления элемента: " + array);
    alert("Массив после удаления элемента: " + array);
} else {
    console.log("Массив остается без изменений: " + array);
    alert("Массив остается без изменений: " + array);
}